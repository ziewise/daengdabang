#!/usr/bin/env node
/**
 * gen-hero-manifest — 히어로 영상 폴더를 스캔해 "영상 + 등장 견종" 매니페스트 자동 생성
 * ---------------------------------------------------------------------
 * 배경:
 *   히어로 배지에 "영상 속 강아지 견종"을 맞추려면 영상→견종 매핑이 필요한데,
 *   파일명에 견종을 넣어두면(아래 규칙) 이 스크립트가 폴더를 스캔해 매핑을
 *   자동 생성한다. → 영상을 추가/교체할 때 파일명만 규칙대로 두면 코드 수정 불필요.
 *
 * 파일명 규칙: {season}_{time}_{weather}_{breed}_{device}.mp4
 *   · 예) summer_night_cloudy_welshcorgi_m.mp4
 *   · breed = public/images/breeds 의 파일명과 동일(welshcorgi / englishbulldog / poodle …)
 *   · 견종 생략한 구형({season}_{time}_{weather}_{device}.mp4)은 breed=null → 기본 견종 폴백
 *
 * 출력: lib/hero-video-manifest.ts  (자동 생성물, 직접 수정 금지)
 *   형태: { "{time}_{weather}": { breed, pc, m } }
 *   · time/weather 가 같으면 PC·모바일이 같은 장면이므로 한 항목으로 묶고 견종 공유
 *
 * 실행: node scripts/gen-hero-manifest.mjs  (npm run dev/build 시 자동 — package.json predev/prebuild)
 */
import { readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const HERO_DIR = join(root, "public/images/hero");
const OUT = join(root, "lib/hero-video-manifest.ts");

const files = readdirSync(HERO_DIR).filter((f) => f.endsWith(".mp4"));

/** @type {Record<string, { breed: string | null, pc: string | null, m: string | null }>} */
const manifest = {};

for (const file of files) {
    const parts = file.replace(/\.mp4$/, "").split("_");
    if (parts.length < 4) continue; // 규칙 밖 파일은 스킵

    const device = parts[parts.length - 1]; // pc | m (항상 마지막)
    const time = parts[1]; // season(0) _ time(1) _ weather(2) ...
    const weather = parts[2];
    // 시간/날씨 뒤, 기기 앞에 토큰이 있으면 견종(여러 토큰이면 '_'로 합침)
    const breed = parts.length >= 5 ? parts.slice(3, -1).join("_") : null;

    const key = `${time}_${weather}`;
    manifest[key] ??= { breed: null, pc: null, m: null };
    manifest[key][device] = `/images/hero/${file}`;
    if (breed) manifest[key].breed = breed; // 견종은 PC/모바일 중 하나라도 있으면 채움
}

const banner =
    "// AUTO-GENERATED by scripts/gen-hero-manifest.mjs — 직접 수정하지 말 것.\n" +
    '// public/images/hero 스캔 결과: "{시간}_{날씨}" → { breed, pc, m }.\n' +
    "// 영상 추가/교체 후 `node scripts/gen-hero-manifest.mjs`(또는 npm run dev/build)로 갱신됨.\n";

writeFileSync(
    OUT,
    `${banner}export const HERO_VIDEO_MANIFEST = ${JSON.stringify(manifest, null, 4)} as const;\n`,
);

console.log(`✓ hero-video-manifest.ts 생성: ${Object.keys(manifest).length} 장면 / ${files.length} 파일`);
